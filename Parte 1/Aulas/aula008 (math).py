from math import sqrt, ceil

num = int(input('digite um num: '))
raiz = sqrt(num) # sqrt > raiz quadrada
print(f'A raiz quadrade de \033[33m{num}\033[m é \033[32m{ceil(raiz)}\033[m.')
# ceil > arredondar pra cima (substitui formatação)
